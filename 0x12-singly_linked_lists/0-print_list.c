#include "lists.h"
#include <stdio.h>

/**
 * print_list - function that prints whats in the list
 * @h: a pointer struct
 * Return: size of return
 */

size_t print_list(const list_t *h)
{
	size_t count = 0;

	while (h != NULL)
	{
		if (h->str != NULL)
		{
			printf("[%d] %s\n", h->len, h->str);
		}
		else
		{
			printf("[0] (nil)\n");
		}
		count++;
		h = h->next;
	}
	return (count);
}
