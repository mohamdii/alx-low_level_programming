#include "calc.h"
/**
 * op_add - returns product of two numbers
 * @a: int parameter
 * @b: int parameter
 *
 * Return: (@a * @b)
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - returns product of two numbers
 * @a: int parameter
 * @b: int parameter
 *
 * Return: (@a * @b)
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - returns product of two numbers
 * @a: int parameter
 * @b: int parameter
 *
 * Return: (@a * @b)
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - returns product of two numbers
 * @a: int parameter
 * @b: int parameter
 *
 * Return: (@a * @b)
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - returns product of two numbers
 * @a: int parameter
 * @b: int parameter
 *
 * Return: (@a * @b)
 */
int op_mod(int a, int b)
{
	return (a % b);
}
